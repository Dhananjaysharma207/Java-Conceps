package com.dev;

public class Calculator {
	public int getAdd(int a, int b) {
		int c=a+b;
		return c;
	}
	public int getMul(int a, int b) {
		int c=a*b;
		return c;
	}
	public int getDiv(int a, int b) {
		int c=a/b;
		return c;
	}
	public int getSub(int a, int b) {
		int c=a-b;
		return c;
		}
	public int getRem(int a, int b) {
		int c = a%b;
		return c;
	}
public int adding(int a, int b) {
	return a+b;
}
public double adding(double a,double b) {
	return a+b;
}
}
